
public class Ex04증감연산자 {

	public static void main(String[] args) {

		
		// 증감 연산자 (단항 연산자) ++ --
		int opr = 0;
		opr += 3;
		
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--); //5
		System.out.println(--opr); //4-1 = 3
		System.out.println(opr++); //3
		System.out.println(++opr); // 4 +1 = 5
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
