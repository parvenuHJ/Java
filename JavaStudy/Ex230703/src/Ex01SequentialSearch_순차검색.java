
public class Ex01SequentialSearch_순차검색 {

	public static void main(String[] args) {

		int [] array = {13,35,15,11,26,72,26,78,19,61,90};
		
		// 내가 찾고싶은 데이터
		int search = 78;
		
		for(int i = 0 ; i<array.length ; i++) {
			if(array[i] == search) {
				System.out.print(search+ "는 " + i + " 번 인덱스에 있습니다.");
			}
		} // 비효율적 --> 맞는 데이터를 찾을때 까지 계속 for 문이 돌아야함 ( 1000개의 인덱스가 있을경우 오래걸림)
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
